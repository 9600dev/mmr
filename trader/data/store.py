from dataclasses import dataclass
from typing import Any, Optional
import datetime as dt
import pandas as pd


@dataclass
class DateRange:
    """Date range for time-series queries."""
    start: dt.datetime
    end: dt.datetime


class DataStore:
    """Abstract interface for time-series data storage."""
    def read(self, symbol: str, start: Optional[dt.datetime] = None, end: Optional[dt.datetime] = None) -> pd.DataFrame: ...
    def write(self, symbol: str, df: pd.DataFrame) -> None: ...
    def delete(self, symbol: str) -> None: ...
    def list_symbols(self) -> list[str]: ...
    def min_date(self, symbol: str) -> dt.datetime: ...
    def max_date(self, symbol: str) -> dt.datetime: ...


class ObjectStore:
    """Abstract interface for key-value object storage."""
    def read(self, key: str) -> Any: ...
    def write(self, key: str, data: Any, **kwargs) -> None: ...
    def delete(self, key: str) -> None: ...
    def list_symbols(self) -> list[str]: ...
